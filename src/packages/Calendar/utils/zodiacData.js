export function getZodiacByDate(month, day) {
  for (const zodiac of zodiacData) {
    if (isDateInRange(month, day, zodiac.startMonth, zodiac.startDay, zodiac.endMonth, zodiac.endDay)) {
      return zodiac
    }
  }
  return null
}

function isDateInRange(month, day, startMonth, startDay, endMonth, endDay) {
  if (startMonth <= endMonth) {
    return (month > startMonth || (month === startMonth && day >= startDay)) &&
           (month < endMonth || (month === endMonth && day <= endDay))
  } else {
    return (month > startMonth || (month === startMonth && day >= startDay)) ||
           (month < endMonth || (month === endMonth && day <= endDay))
  }
}
